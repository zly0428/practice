	/** Find the path from start to goal using breadth first search
	 * 
	 * @param start The starting location
	 * @param goal The goal location
	 * @param nodeSearched A hook for visualization.  See assignment instructions for how to use it.
	 * @return The list of intersections that form the shortest (unweighted)
	 *   path from start to goal (including both start and goal).
	 */
	public List<GeographicPoint> bfs(GeographicPoint start, 
			 					     GeographicPoint goal, Consumer<GeographicPoint> nodeSearched)
	{
		// TODO: Implement this method in WEEK 2
		// manipulating the bfs with MapNode
		MapNode s = myGraph.get(start);
		MapNode g = myGraph.get(goal);
		
		// return null if the Node is not added into the graph
		if (s == null || g == null) {
			System.out.println("Start or goal MapNode is null!  No path exists.");
			return null;
		}
		
		// initialize the queue, visited set, and path
		HashSet<MapNode> visited = new HashSet<MapNode>();
		Queue<MapNode> toExplore = new LinkedList<MapNode>();
		HashMap<MapNode, MapNode> parentMap = new HashMap<MapNode, MapNode>();
		
		toExplore.add(s);
		boolean found = false;
		while (!toExplore.isEmpty()) {
			MapNode curr = toExplore.remove();
			
			// find it
			if ( curr == g ) {
				found = true;
				break;
			}
			// get the neighbours and do the traverse
			List<MapNode> neighbors = curr.getNeighbors();
			ListIterator<MapNode> it = neighbors.listIterator(neighbors.size());
			while (it.hasPrevious()) {
				MapNode next = it.previous();
				nodeSearched.accept(next.getLocation());
				if (!visited.contains(next)) {
					visited.add(next);
					parentMap.put(next, curr);
					toExplore.add(next);
				}
			}
		}
		
		if (!found) {
			System.out.println("No path exists");
			return null;
		}
		// reconstruct the path in order and convert the MapNode to the GeographicPoint
		LinkedList<MapNode> path = new LinkedList<MapNode>();
		ArrayList<GeographicPoint> res = new ArrayList<GeographicPoint>();
		MapNode curr = g;
		while (curr != s) {
			path.addFirst(curr);
			curr = parentMap.get(curr);
		}
		path.addFirst(s);
		for ( MapNode n : path ) {
			res.add(n.getLocation());
		}
		return res;
		// Hook for visualization.  See writeup.
		//nodeSearched.accept(next.getLocation());


	}
	

